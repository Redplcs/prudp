/*
{Group:System Callbacks}

Summary: Called by the system when the duplicated object has been
initialized.

Remarks: When InitDO is called, the Duplication role of the
DuplicatedObject is fully defined.  If it is a Duplica, all its
datasets have been appropriately initialized.

This section explains how to use the InitDO system callback.

Suppose we want to create a $gameORapp$ where $AvatarORInformationObject$s appear at random
locations.  We also want to render the graphical representation of the
$AvatarORInformationObject$s at the same location in 3D space on all the stations
immediately when they join the session.

Lets say the object '$AvatarORInformationObject$' contains a position dataset defined in the DDL file as follows:

      dataset Position {
	      double x;
	      double y;
	      double z;
      };

When creating an $AvatarORInformationObject$ via DuplicatedObject::Create, a Duplication
master is created, so all we need to do before rendering it is set the
values of its position dataset. When the $AvatarORInformationObject$ is published (via
DuplicatedObject::Publish), its Duplica appear on all the stations that require a copy of the object
which then need to be rendered at the correct position.  Thus, setting the
$AvatarORInformationObject$ position from within the InitDO function would be done as follows:

      void $AvatarORInformationObject$::InitDO(void) {
          if ( IsADuplicationMaster() )
          {
              Position.x = SomeRandomValue();
              Position.y = SomeRandomValue();
              Position.z = SomeRandomValue();
          }

          RenderAtCurrentPosition();
      }

Note: For this example, because the datasets have default values when
the $AvatarORInformationObject$ object is instantiated, setting the $AvatarORInformationObject$ position from
within the constructor will _not_ work.

See Also: DuplicatedObject::Create, DuplicatedObject::Publish, IsADuplicationMaster, IsADuplica
*/
		void InitDO();
		
/*
{Group:System Callbacks}

Summary: Called by the system to determine the distance between two
duplicated objects.

Parameters:
pObservedDuplicatedObject: pointer to the duplicated object which you want to compute the
distance to.  

Remarks: When distance-based dead reckoning is invoked, the desired precision of the extrapolation 
depends on a distance between the duplicated objects. The distance is a value 
specific to the $gameORapp$ computed via this system callback and is typically the distance
between the position of the objects in the virtual world.

For distance-based dead reckoning $PRODUCTNAME$ needs to compute the distance
between the registered observer (register via Station::RegisterObserver
and get via Station::GetObserver) and the Duplica of the 
object for which it is doing the distance-based dead reckoning.  Thus, $PRODUCTNAME$
calls this method on the Duplica of the observer and passes to the function a pointer 
to the duplicated object for which it is determining the allowable error in the
extrapolation. The distance must be a positive value, or the special value
UNKNOWN_DISTANCE. The default implementation returns UNKNOWN_DISTANCE.

The following is an example that computes the distance between two
Spheres in a virtual world.  The context of this example is found in the
Dead Reckoning Overview.

			double Sphere::ComputeDistance(DuplicatedObject * pObservedDuplicatedObject) {
				switch (pObservedDuplicatedObject->GetClassID()) {
					case DOCLASSID (Sphere):
						return sqrt( sqr(m_Pos.x-((Sphere*)pObservedDuplicatedObject)->m_Pos.x)
									 +sqr(m_Pos.y-((Sphere*)pObservedDuplicatedObject)->m_Pos.y));
					default:
						return UNKNOWN_DISTANCE ;
				}
			}

See Also: Dead Reckoning, Dead Reckoning Overview, ErrorToleranceFunction, UNKNOWN_DISTANCE, 
Station::RegisterObserver, PHBDRParameters
*/
		double ComputeDistance(DuplicatedObject* pObservedDuplicatedObject);
		
