/*
Description: The class DuplicatedObject is the root of all user-defined duplicated
objects.  If a DOClass is defined in the DDL, the corresponding
DOClass generated by the DDL compiler will ultimately inherit from the
DuplicatedObject class.
Remarks: The DuplicatedObject class contains regular methods, inherited by
every user doclass.  It also defines system callbacks.  The system
callbacks have the semantic of a virtual method.  If they are
overriden by the user doclass, the overriden method will be called
whenever applicable.
See Also: Actions Overview, Fault Tolerance Overview, Load Balancing Overview
*/
