/*
Summary: Propagates the contents of all the duplicated object
datasets to its duplicas, or if a parameter is supplied it propagates only
the contents of the specified dataset.

Parameters:  
refDataset:  An optional pointer to the specific dataset that is to be updated.  

Remarks:  This method must be called on a Duplication master.  It has the
semantic of a virtual method: it can be called on a base class and
all the derived class datasets will also be updated.  The call can be made
with or without the parameter to specify which of the duplicated object's datasets should
be updated. For example, suppose we have a Position dataset and the m_dsPosition dataset
member of type Position is associated with the Sphere doclass in the DDL file, then it
is possible to update just that Position dataset as follows:

      //assuming that all we have is a pointer to our Sphere duplicated object
      poSphere->Update(poSphere->m_dsPosition);

If a station cannot directly communicate with a station to which it wishes to send a message
$PRODUCTNAME$ will automatically route the message via another station. This effectively deals with 
the problem of stations being unable to communicate with each other due to the fact that they 
have no common transport type.

Note: Depending on the dataset update policy, Update does not necessary
generate a network message. If some of the datasets are extrapolated, Update must be called
regularly (i.e. after DoPhysic)  even if the contents of the datasets did not change. This
is necessary to maintain a coherent prediction model for the
extrapolated datasets.

Returns: true if the update call is successful, false if it signals an error. If this method is 
called on a Duplica the error SYSTEMERROR_OBJDUP_INVALID_ON_DUPLICA will be returned.
SYSTEMERROR_MISSING_DATASET_CALLBACK is also possible.
*/
